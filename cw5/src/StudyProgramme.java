public class StudyProgramme {
    String programme;
    String description;
    int semesters;
    int allowedITN;
    public StudyProgramme(String programme, String description, int semesters, int allowedITN) {
        this.programme = programme;
        this.description = description;
        this.semesters = semesters;
        this.allowedITN = allowedITN;
    }}